ZKEY_PATH="$WORK_DIR/semaphore-4.13.0-10.zkey"

curl -L --fail -o "$ZKEY_PATH" "$ZKEY_URL"
npx -y snarkjs zkey export verificationkey "$ZKEY_PATH" "$WORK_DIR/verification_key.json"

node -e '
